# declarar um dicionario
dados = {}
print(dados)

# Adicionar dados no dicionario
dados = {
    '001': 'item exy'
}
print(dados)

# Outra forma de adicionar dados no dicionario
dados['002']
print(dados)

# Acessando um item especifico do dicionario, passando a chave do item
print(dados['002'])